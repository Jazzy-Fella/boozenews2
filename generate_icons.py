SRC_W, SRC_H = 16, 16

def make_icon(size):
    img = Image.new('RGB', (size, size), (0, 0, 0))
    draw = ImageDraw.Draw(img)
    padding = size * 0.10
    avail = size - padding * 2
    scale = avail / SRC_W
    ox = (size - SRC_W * scale) / 2
    oy = (size - SRC_H * scale) / 2
    for x, y, w, h, color in RECTS:
        x1 = int(ox + x * scale)
        y1 = int(oy + y * scale)
        x2 = int(ox + (x + w) * scale)
        y2 = int(oy + (y + h) * scale)
        draw.rectangle([x1, y1, x2 - 1, y2 - 1], fill=hex_to_rgb(color))
    return img

